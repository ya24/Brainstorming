package cn.gduf.brainstorming.model.vo;
/**
 * 关注表实体类
 */
public class Focus {
	private String focusID;
	private String beFocusID;
	
	public Focus(){}

	public String getFocusID() {
		return focusID;
	}

	public void setFocusID(String focusID) {
		this.focusID = focusID;
	}

	public String getBeFocusID() {
		return beFocusID;
	}

	public void setBeFocusID(String beFocusID) {
		this.beFocusID = beFocusID;
	}
	
	
}
